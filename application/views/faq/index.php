        <h3 id='faq1'>What is PhotoQuest?</h3>
        <p>It is a photography game. The site gives you a 'quest', and you have 
            one day to complete it.</p>
        
        <h3 id='faq2'>What is a quest?</h3>
        <p>It is a theme for shooting. The quest must be one word only. Your goal 
            is to take a photo that you associate with the theme.</p>
        
        <h3 id='faq3'>I can't upload a photo, why?</h3>
        <p>You can upload only one submission for every quest. This is not a general 
            gallery web site. If you have taken multiple shots for today's quest 
            you must choose only one to submit</p>
        
        <h3 id='faq4'>How can I choose the quest I'm uploading submission to?</h3>
        <p>You are always submitting for today's topic. You can't submit for older 
            topic or that of tomorrow. This is part of the game - you have topic 
            for today and you have only that day to plan the shoot and take it.</p>
        
        <h3 id='faq5'>Can I submit photomanipulations?</h3>
        <p>Of course. You can use Photoshop, Gimp or whatever software you have. 
            You can upload HDRs, too. However, do not forget this is a photography 
            game, so avoid to submit collages, comics, drawn art or so :) Also avoid 
            putting frames or text on your shots. Watermark is fine if it is non-obtrusive.</p>
        
        <h3 id='faq6'>Who choose the topics?</h3>
        <p>They are randomly drawn from a database of words. The algorithm is as 
            follows: get todays day of month. Turn it into the corresponding letter 
            of the alphabet. This is the topic's starting letter. The system choose 
            a random word from a predefined set of words starting with that letter. 
            In that way 1st of the month gets a topic starting with 'a', 2nd date 
            gets topic starting with 'b' and so on. However, on 27th, 28th, 29th 
            and 30th of the month there are no letters and these topics are picked 
            by the editors.</p>
        
        <h3 id='faq7'>What is the appropriate content for submissions?</h3>
        <p>Everything that is not illegal, immoral or not yours.</p>
        
        <h3 id='faq8'>I have a cool idea for a topic! Can you put it in?</h3>
        <p>You can use the 'Suggest topic' box up there :) The topic is sent to 
            the editors for approval and if it is okay it gets added to the database. 
            However, have in mind that the approval process can take a few days, 
            so don't send a topic for tomorrow quest and expect to have it there 
            immediately :)</p>
        
        <h3 id='faq9'>Why do I see tomorrow's topic?</h3>
        <p>Tomorrow's topic is selected at noon at the present day. In that way 
            you have a little more time to think if you are going to participate 
            in it and come with a general idea about your submission.</p>
        
        <h3 id='faq10'>Can I get a new topic notificaiton?</h3>
        <p>Yes. There are RSS feeds and e-mail notifications available.</p>
        
        <h3 id='faq11'>Something is not working!</h3>
        <p>The site is very fresh and it is in beta-testing stage. Some features 
            might not be working, other might change with time. However, we are 
            trying to keep things stable, so if you notice something that doesn't 
            behave correctly, it is probably because we had missed it. We will be 
            very happy if you send bug reports to ivanatora@gmail.com</p>
        
        <h3 id='faq12'>I don't have DSLR, can I still play?</h3>
        <p>Of course, if you have a compact camera or cameraphone or other means 
            of shooting a picture. It does not matter what gears are used to take 
            a photo. Sometimes better photos are made with simplier technology. It 
            is not the camera that makes the photo, it is the man behind :)</p>
        
        <h3 id='faq13'>I missed the deadline for the last quest! Can I still submit to it?</h3>
        <p>Sorry, no. You can submit up to 23:59 each day. This is a part of the 
            challenge - to take the photo, tweak it and upload it the same day. Don't 
            worry, every day there is a new topic, it is no big deal to miss one.</p>
